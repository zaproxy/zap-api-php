namespace Zap;

use PHPUnit\Framework\TestCase;

final class ZapErrorTest extends TestCase
{
    public function testCanCreateErrorWithMessage()
    {
        $expectedString = __CLASS__ . ": [42]: Test Message\n";
        $this->assertEquals(
            $expectedString,
            (string) new ZapError("Test Message", 42)
        );
    }
}
